package main

import "fmt"

func main() {
	// creating and initializing a variable with a string
	My_value_1 := "Hello World"

	// Using var keyword
	var My_value_2 string
	My_value_2 = "Welcome to Mother Earth"

	// Displaying strings
	fmt.Println("String 1: ", My_value_1)
	fmt.Println("String 2: ", My_value_2)
}
