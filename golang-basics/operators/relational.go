package main

import "fmt"

func main() {
	// Relational operators are used for comparison of two values
	p := 34
	q := 20

	// Equal to
	result1 := p == q
	fmt.Println(result1)

	// Not Equal to
	result2 := p != q
	fmt.Println(result2)

	// Less than
	result3 := p < q
	fmt.Println(result3)

	// Greater than
	result4 := p > q
	fmt.Println(result4)

	// Greater than equal to
	result5 := p >= q
	fmt.Println(result5)

	// Less than equal to
	result6 := p <= q
	fmt.Println(result6)
}
